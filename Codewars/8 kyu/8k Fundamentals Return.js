function add(a, b) {
  return a + b;
} // add()

function divide(a, b) {
  return a / b;
} // divide()

function multiply(a, b) {
  return a * b;
} // multiply()

function mod(a, b) {
  return a % b;
} // mod()

function exponent(a, b) {
  return a ** b;
} // exponent()

function subt(a, b) {
  return a - b;
} // subt()

console.log(add(1, 2));
console.log(multiply(1, 2));
console.log(divide(2, 1));
console.log(mod(1, 2));
console.log(exponent(1, 2));
console.log(subt(1, 2));
